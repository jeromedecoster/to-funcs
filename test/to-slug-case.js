const fn = require('../to-slug-case')
const test = require('tape')

test('to-slug-case', function (t) {

  t.equal(fn('thisIsAString'),          'this-is-a-string')
  t.equal(fn('THIS_IS_A_STRING'),       'this-is-a-string')
  t.equal(fn('this.is.a.string'),       'this-is-a-string')
  t.equal(fn('ThisIsAString'),          'this-is-a-string')
  t.equal(fn('This is a string.'),      'this-is-a-string')
  t.equal(fn('this_is_a_string'),       'this-is-a-string')
  t.equal(fn('this is a string'),       'this-is-a-string')
  t.equal(fn('This Is a String'),       'this-is-a-string')
  t.equal(fn('-this__is$%a-string...'), 'this-is-a-string')

  t.equal(fn('foo'),             'foo')
  t.equal(fn('foo-bar'),         'foo-bar')
  t.equal(fn('foo-bar-baz'),     'foo-bar-baz')
  t.equal(fn('foo--bar'),        'foo-bar')
  t.equal(fn('--foo-bar'),       'foo-bar')
  t.equal(fn('--foo--bar'),      'foo-bar')
  t.equal(fn('FOO-BAR'),         'foo-bar')
  t.equal(fn('FOÈ-BAR'),         'foe-bar')
  t.equal(fn('-foo-bar-'),       'foo-bar')
  t.equal(fn('--foo--bar--'),    'foo-bar')
  t.equal(fn('foo.bar'),         'foo-bar')
  t.equal(fn('foo..bar'),        'foo-bar')
  t.equal(fn('..foo..bar..'),    'foo-bar')
  t.equal(fn('foo_bar'),         'foo-bar')
  t.equal(fn('__foo__bar__'),    'foo-bar')
  t.equal(fn('__foo__bar__'),    'foo-bar')
  t.equal(fn('foo bar'),         'foo-bar')
  t.equal(fn('  foo  bar  '),    'foo-bar')
  t.equal(fn('-'),               '')
  t.equal(fn(' - '),             '')
  t.equal(fn('fooBar'),          'foo-bar')
  t.equal(fn('fooBar-baz'),      'foo-bar-baz')
  t.equal(fn('foìBar-baz'),      'foi-bar-baz')
  t.equal(fn('fooBarBaz-bazzy'), 'foo-bar-baz-bazzy')
  t.equal(fn('FBBazzy'),         'f-b-bazzy')
  t.equal(fn('F'),               'f')
  t.equal(fn('FBBÈzzy'),         'f-b-b-ezzy')
  t.equal(fn('FooBar'),          'foo-bar')
  t.equal(fn('Foo'),             'foo')
  t.equal(fn('FOO'),             'foo')
  t.equal(fn('foo bar'),         'foo-bar')
  t.equal(fn('foo -bar'),        'foo-bar')
  t.equal(fn('foo -bar baz'),    'foo-bar-baz')
  t.equal(fn('', ''),            '')
  t.equal(fn('--'),              '')
  t.equal(fn(''),                '')
  t.equal(fn('--__--_--_'),      '')
  t.equal(fn('---_ --  -_- '),   '')
  t.equal(fn('foo bar?'),        'foo-bar')
  t.equal(fn('foo bar!'),        'foo-bar')
  t.equal(fn('foo bar$'),        'foo-bar')
  t.equal(fn('foo-bar#'),        'foo-bar')

  t.equal(fn(),          '')
  t.equal(fn(null),      '')
  t.equal(fn(undefined), '')
  t.equal(fn(0),         '')
  t.equal(fn(1),         '')
  t.equal(fn(true),      '')
  t.equal(fn(false),     '')
  t.equal(fn([]),        '')
  t.equal(fn([1]),       '')
  t.equal(fn({a:1}),     '')

  t.end()
})
