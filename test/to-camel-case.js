const fn = require('../to-camel-case')
const test = require('tape')

test('to-camel-case', function (t) {

  // from https://github.com/ianstormtaylor/to-camel-case/blob/master/test/index.js
  t.equal(fn('thisIsAString'),          'thisIsAString')
  t.equal(fn('THIS_IS_A_STRING'),       'thisIsAString')
  t.equal(fn('this.is.a.string'),       'thisIsAString')
  t.equal(fn('ThisIsAString'),          'thisIsAString')
  t.equal(fn('This is a string.'),      'thisIsAString')
  t.equal(fn('this_is_a_string'),       'thisIsAString')
  t.equal(fn('this is a string'),       'thisIsAString')
  t.equal(fn('This Is a String'),       'thisIsAString')
  t.equal(fn('-this__is$%a-string...'), 'thisIsAString')

  // from https://github.com/sindresorhus/camelcase/blob/master/test.js
  t.equal(fn('foo'),             'foo')
  t.equal(fn('foo-bar'),         'fooBar')
  t.equal(fn('foo-bar-baz'),     'fooBarBaz')
  t.equal(fn('foo--bar'),        'fooBar')
  t.equal(fn('--foo-bar'),       'fooBar')
  t.equal(fn('--foo--bar'),      'fooBar')
  t.equal(fn('FOO-BAR'),         'fooBar')
  t.equal(fn('FO√à-BAR'),         'foeBar')
  t.equal(fn('-foo-bar-'),       'fooBar')
  t.equal(fn('--foo--bar--'),    'fooBar')
  t.equal(fn('foo.bar'),         'fooBar')
  t.equal(fn('foo..bar'),        'fooBar')
  t.equal(fn('..foo..bar..'),    'fooBar')
  t.equal(fn('foo_bar'),         'fooBar')
  t.equal(fn('__foo__bar__'),    'fooBar')
  t.equal(fn('__foo__bar__'),    'fooBar')
  t.equal(fn('foo bar'),         'fooBar')
  t.equal(fn('  foo  bar  '),    'fooBar')
  t.equal(fn('-'),               '')
  t.equal(fn(' - '),             '')
  t.equal(fn('fooBar'),          'fooBar')
  t.equal(fn('fooBar-baz'),      'fooBarBaz')
  t.equal(fn('fo√¨Bar-baz'),      'foiBarBaz')
  t.equal(fn('fooBarBaz-bazzy'), 'fooBarBazBazzy')
  t.equal(fn('FBBazzy'),         'fBBazzy')
  t.equal(fn('F'),               'f')
  t.equal(fn('FBB√àzzy'),         'fBBEzzy')
  t.equal(fn('FooBar'),          'fooBar')
  t.equal(fn('Foo'),             'foo')
  t.equal(fn('FOO'),             'foo')
  t.equal(fn('foo bar'),         'fooBar')
  t.equal(fn('foo -bar'),        'fooBar')
  t.equal(fn('foo -bar baz'),    'fooBarBaz')
  t.equal(fn('', ''),            '')
  t.equal(fn('--'),              '')
  t.equal(fn(''),                '')
  t.equal(fn('--__--_--_'),      '')
  t.equal(fn('---_ --  -_- '),   '')
  t.equal(fn('foo bar?'),        'fooBar')
  t.equal(fn('foo bar!'),        'fooBar')
  t.equal(fn('foo bar$'),        'fooBar')
  t.equal(fn('foo-bar#'),        'fooBar')
  t.equal(fn('foo-bar-üí£ÿø'),     'fooBar')

  t.equal(fn('h√™ll√∏‚Äêw√∂rld'),     'helloWorld')
  t.equal(fn('h√™ll√∏‚Äêw√∂rld-üí£ÿø'), 'helloWorld')
  t.equal(fn('üí£-ÿø'),            '')

  t.equal(fn(),          '')
  t.equal(fn(null),      '')
  t.equal(fn(undefined), '')
  t.equal(fn(0),         '')
  t.equal(fn(1),         '')
  t.equal(fn(true),      '')
  t.equal(fn(false),     '')
  t.equal(fn([]),        '')
  t.equal(fn([1]),       '')
  t.equal(fn({a:1}),     '')

  t.end()
})
